const V1_ID_TO_NAME = new Map<string, string>(V1_STOCK_EXERCISES.map((e) => [e.id, e.name]));
const norm = (n: string) => n.toLowerCase().replace(/\s+/g, ' ').trim();

/** Map old v1 id → current catalog id, using exercise display name. */
export function mapV1ExerciseIdToV2(legacyId: string, v2Exercises: readonly { id: string; name: string }[]): string {
  if (v2Exercises.some((e) => e.id === legacyId)) return legacyId;
  const legacyName = V1_ID_TO_NAME.get(legacyId);
  if (!legacyName) return legacyId;
  const target = norm(legacyName);
  const hit = v2Exercises.find((e) => norm(e.name) === target);
  return hit?.id ?? legacyId;
}
